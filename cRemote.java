public class cRemote {

    void passosAteAqui() {
        /**
         * git init (repositório local)     
         * 
         * git clone url nomeProjeto
         *      Clonar o repositório é git clone    
         */
    }    

    void listarBranchs() {
        /**
         * git branch -v: listar branchs locais.
         * 
         * git branch -r: listar branchs remotas.
         * 
         * git branch -a: listar branchs locais e remotas.
         */
    }

    void push() {
        /**
         * git clone url
         *  
         *      > isso vai clonar a master.
         * 
         * git checkout -b branch 
         *      > -b é pra criar, caso não exista vai criar.
         * 
         * git fetch -p
         *      > vai deixar as branchs iguais ao do remoto.
         * 
         * git push -u origin branch
         *      > vai dar push na branch que eu escolher
         */
    }

    
    	
}
